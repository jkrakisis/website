jQuery(document).ready(function($)
{
	try
	{
	
		$('div.list >ul').children().each(function (i)
		{
			if (i>0) { 
				$(this).hide();
			}
			else {
				$(this).show(); 
			}
		});
	
		$('div.list').find('a').click(function()
		{
			$('div.list >ul').children().hide();
			
			$($(this).attr('href')).show();
			
			return false;
		});
		
	}
	catch (e)
	{
		
	}

});
